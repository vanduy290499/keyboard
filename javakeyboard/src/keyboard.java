    @Override
    public void run() {
        try {
            while (RUN_TIME) {
                tic++;
                if (tic == 100) {
                    sec += 1;
                    tic = 0;
                }
                if (sec == 60) {
                    min += 1;
                    sec = 0;
                }
                if (min == 60) {
                    hour += 1;
                    min = 0;
                }
                blTic.setText(String.valueOf(tic));
                blSec.setText(String.valueOf(sec));
                blMin.setText(String.valueOf(min));
                blHour.setText(String.valueOf(hour));
                Thread.sleep(10);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

